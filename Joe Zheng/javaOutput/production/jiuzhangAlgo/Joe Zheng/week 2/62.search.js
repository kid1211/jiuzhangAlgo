/**
 * @param A: an integer rotated sorted array
 * @param target: an integer to be searched
 * @return: an integer
 */

const search = function(A, target) {
  if (
    A === null ||
    A === undefined ||
    A.length <= 0 ||
    target === null ||
    target === undefined
  ) {
    return -1;
  }

  let oriLenght = A.length;
  if (oriLenght == 1) {
    return 0;
  }

  // console.log("4.", 0 == search([1, 2, 3, 4, 5], 1));
  let peakIndex = findMin(A);
  // console.log("*****", A[peakIndex]);
  A = [...A, ...A];
  //   return -1;
  let left = peakIndex;
  let right = peakIndex + oriLenght;

  while (left + 1 < right) {
    let mid = Math.floor(left + (right - left) / 2);
    if (A[mid] == target) {
      return mid > oriLenght ? mid - oriLenght : mid;
    } else if (A[mid] > target) {
      right = mid;
    } else {
      left = mid;
    }

    // var print = [];
    // for (var i = left; i < right; i++) {
    //   print.push(A[i]);
    // }
    // console.log(A[mid], print);
  }

  if (A[left] == target) {
    return left >= oriLenght ? left - oriLenght : left;
  }
  if (A[right] == target) {
    return right >= oriLenght ? right - oriLenght : right;
  }

  return -1;
};

const findMin = function(nums) {
  if (nums === null || nums === undefined || nums.length <= 0) {
    return null;
  }

  if (nums.length == 1) {
    return 0;
  }

  let left = 0;
  let right = nums.length - 1;

  while (left + 1 < right) {
    let mid = Math.floor(left + (right - left) / 2);

    if (nums[mid] < nums[mid - 1]) {
      return mid;
    }

    if (nums[right] > nums[mid]) {
      right = mid;
    } else if (nums[left] < nums[mid]) {
      left = mid;
    } else {
      //   console.log(left, mid, right);
      //   console.log(nums[left], nums[mid], nums[right]);
      return null;
    }
  }

  return nums[left] < nums[right] ? left : right;
};

